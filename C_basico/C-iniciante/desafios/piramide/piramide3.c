#include <stdio.h>

int main() {
    char matriz[5][11] = {
        {'*', '*', '*', '*', '*', ' ', '*', '*', '*', '*', '*'},
        {'*', '*', '*', '*', ' ', ' ', ' ', '*', '*', '*', '*'},
        {'*', '*', '*', ' ', ' ', ' ', ' ', ' ', '*', '*', '*'},
        {'*', '*', ' ', ' ', ' ', ' ', ' ', ' ', ' ', '*', '*'},
        {'*', ' ', ' ', ' ', ' ', ' ', ' ', ' ', ' ', ' ', '*'},
    };

    int i = 0;

    while (i < 5) {
        int j = 0;
        while (j < 11) {
            printf("%c", matriz[i][j]);
            j++;
        }
        i++;
        printf("\n");
    }

    return 0;
}